DO $$
BEGIN
CREATE TABLE IF NOT EXISTS core.transaction_receipt_counter (   
    id uuid NOT NULL,
    receipt_id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    create_date timestamp without time zone NOT NULL,
    update_date timestamp without time zone,
    created_by varchar(50) NOT NULL,
    last_modified_by varchar(50) ,
    record_status varchar(50)NOT NULL,
    CONSTRAINT pk_transaction_receipt_counter PRIMARY KEY (id)
);
END $$;

CREATE UNIQUE INDEX ix_transaction_receipt_counter_receipt_id ON core.transaction_receipt_counter (receipt_id);